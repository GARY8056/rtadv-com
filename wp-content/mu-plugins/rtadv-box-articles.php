<h2>袖套盒常見尺寸與公差問題</h2>

<p>袖套盒最核心的工程挑戰在於：<strong>套筒與內盒之間的公差配合（Tolerance Fit）</strong>。公差過緊，消費者拉不出內盒；公差過鬆，內盒在運輸中自行脫落，造成損壞。</p>

<h3>標準公差建議值</h3>
<ul>
  <li>紙板袖套盒（銅版紙/特種紙）：套筒內徑比內盒外徑大 <strong>1.5–2mm</strong> 為佳</li>
  <li>灰板裱糊精裝袖套盒：套筒內徑比內盒外徑大 <strong>2–3mm</strong>（灰板較厚，裱糊後尺寸微幅變化）</li>
  <li>絨布裱糊版：建議再放寬 0.5mm，避免布料摩擦造成緊澀</li>
</ul>

<h2>踩坑警告：袖套盒常見設計失誤</h2>

<p>根據圓廣印刷（rtadv.com）實務資料，以下是袖套盒專案中最常見的三類錯誤：</p>

<p>❌ <strong>問題一：套筒尺寸直接照產品尺寸輸入，未計算紙板厚度</strong><br>
✅ <strong>解法</strong>：刀模尺寸需在產品淨尺寸基礎上，加計內盒紙板厚度（通常 0.3–0.5mm）再加公差。建議要求印刷廠提供刀模圖確認。</p>

<p>❌ <strong>問題二：外套筒印刷在摺疊邊緣出現「爆色」（Cracking）</strong><br>
✅ <strong>解法</strong>：折疊線處應減少滿版深色印刷，必要時設計成套準拼版，或改用壓線後加工的方式避免油墨龜裂。</p>

<p>❌ <strong>問題三：客戶自行提供結構圖，卻未標示「裱糊方向」，導致木紋方向錯誤</strong><br>
✅ <strong>解法</strong>：結構設計圖需同時標示紙紋方向（Grain Direction），讓印刷廠在排版時確保彎折方向與紙紋平行，避免開盒時翹曲。</p>

<p>❌ <strong>問題四：只確認外套筒樣品，未同時確認內盒與外套的組裝公差</strong><br>
✅ <strong>解法</strong>：打樣階段要求印刷廠提供「套筒＋內盒」組裝版樣品，實際測試滑動順暢度後再批量生產。</p>

