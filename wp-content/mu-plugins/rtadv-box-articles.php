function rtadv_box_articles_publish() {
	if ( get_option( 'rtadv_box_articles_done_v1' ) ) {
		return;
	}

	$articles = rtadv_get_box_articles();
	$log = [];
	$log[] = '[rtadv-box-articles] started at ' . date( 'Y-m-d H:i:s' );

	foreach ( $articles as $art ) {
		$existing = get_page_by_path( $art['slug'], OBJECT, 'post' );
		if ( $existing ) {
			$log[] = "  slug={$art['slug']} — already exists (ID={$existing->ID}), skipped";
			continue;
		}

		$post_id = wp_insert_post( [
			'post_title'   => $art['title'],
			'post_name'    => $art['slug'],
			'post_content' => $art['content'],
			'post_status'  => 'publish',
			'post_type'    => 'post',
			'post_category' => [ $art['category'] ],
		], true );

		if ( is_wp_error( $post_id ) ) {
			$log[] = "  slug={$art['slug']} — ERROR: " . $post_id->get_error_message();
			continue;
		}

		update_post_meta( $post_id, 'rank_math_focus_keyword', $art['focus_kw'] );
		update_post_meta( $post_id, 'rank_math_title', $art['rm_title'] );
		update_post_meta( $post_id, 'rank_math_description', $art['rm_desc'] );

		$url = get_permalink( $post_id );
		$log[] = "  ID={$post_id} slug={$art['slug']} → {$url}";
	}

	update_option( 'rtadv_box_articles_done_v1', true );
	$log[] = '[rtadv-box-articles] completed';

	$log_file = WP_CONTENT_DIR . '/rtadv-box-articles.log';
	file_put_contents( $log_file, implode( "\n", $log ) . "\n" );

	@unlink( __FILE__ );
}

