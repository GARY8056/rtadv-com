<p>內抽屜是裝載產品的托盤，可搭配 EVA 內衬、吸塑固定座、紙托等內插件固定產品。外側面常附一個「推出孔」或絲帶拉帶，方便消費者拉出抽屜。抽屜本體材質通常與外殼相同（灰板裱紙），或使用輕量折疊紙板版（降低成本）。</p>

