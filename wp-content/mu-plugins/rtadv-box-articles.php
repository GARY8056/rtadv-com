<h2>常見設計踩坑與解法</h2>

<p>以下整理台灣包裝廠商實務中最常見的天地盒設計錯誤，以及對應解法：</p>

<p>❌ <strong>問題一：蓋子拆不下來（過緊）</strong><br>
設計師未考量灰板或裱糊紙的實際厚度，導致蓋內徑過小。<br>
✅ <strong>解法</strong>：打樣前先確認灰板厚度（常見 1.5mm / 2mm），蓋內徑設定比底盒外徑大至少 1.0mm，打樣後以實品量測再決定正式尺寸。</p>

<p>❌ <strong>問題二：蓋子容易滑落（過鬆）</strong><br>
折疊紙板版間隙設計沿用精裝版規格，但折疊版紙厚較薄，導致套合不緊。<br>
✅ <strong>解法</strong>：折疊版蓋內徑應比精裝版減少 0.3–0.5mm；若要加強固定，可在底盒外壁加貼一圈薄泡棉（1–2mm）增加摩擦。</p>

<p>❌ <strong>問題三：裱糊紙在轉角處起皺或脫膠</strong><br>
灰板轉角弧度不足、裱糊紙延展率不夠，或黏合劑品質不佳。<br>
✅ <strong>解法</strong>：精裝版轉角應做成 3–5mm 圓角（rounded corner），選用延展率較佳的特種紙；同時向台灣印刷廠確認使用的黏合劑耐候等級（特別是月餅禮盒需耐中秋前高溫儲存）。</p>

<p>❌ <strong>問題四：印刷顏色在蓋與底出現色差</strong><br>
天地盒蓋與底若在不同批次印刷，可能出現明顯色差，影響整體品質感。<br>
✅ <strong>解法</strong>：要求同批次同時印刷蓋與底的裱糊用紙，並在合約中明訂色差容許值（Delta E ≤ 2）。</p>

<p>❌ <strong>問題五：底盒承重不足（精裝版輸入較重商品）</strong><br>
灰板厚度選用過薄（1.0mm），裝入較重商品（如玻璃瓶保養品、大容量茶罐）後底部變形。<br>
✅ <strong>解法</strong>：商品重量超過 500g 時，建議改用 2mm 以上灰板，或在底盒內加一層瓦楞紙板補強。</p>

